#include <iostream>
using namespace std;
int T;
int arr[12] = {0};
/*
 * 1 : 1  -> 1 -> 1개
 * 2 : 11, 2 -> 1 + 1 -> 2개
 * 3 : 111, 12, 3 -> 1 + 2 + 1 -> 4개
 * 4 : 1111, 112, 22, 13 -> 1 + 3 + 1 + 2 -> 7개 
 * 5 : 11111, 1112, 122, 113, 23 -> 1 + 4 + 3 + 3 + 2 -> 13개
 * 6 : 111111, 11112, 1122, 222, 1113, 33, 123 -> 1 + 5 + 6 + 1 + 4 + 1 + 6 -> 24개
 * 7 : 1111111, 111112, 11122, 1222, 11113, 133, 1123, 223 -> 1 + 6 + 10 + 4 + 5 + 3 + 12 + 3 -> 44개
*/
int main() {
    cin >> T;
    arr[0] = 1; arr[1] = 2; arr[2] = 4;
    for (int i = 3; i < 12; i++) {
        arr[i] = arr[i-1] + arr[i-2] + arr[i-3];
    }

    for (int i = 0; i < T; i++) {
        int n = 0;
        cin >> n;
        cout << arr[n-1] << endl;

    }


    return 0;
}
